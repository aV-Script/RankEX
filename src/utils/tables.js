export function getAgeGroup(testKey, age) {
  const test = ALL_TESTS.find(t => t.key === testKey)
  if (!test?.ageGroup) {
    return age <= 35 ? '18-35' : age <= 45 ? '36-45' : age <= 55 ? '46-55' : age <= 65 ? '56-65' : '66+'
  }
  return test.ageGroup(age)
}